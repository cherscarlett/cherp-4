<script>
import Lightbox from '~/components/Lightbox.vue'

export default {
  mounted() {
    let butts = []
    butts = this.$el.getElementsByTagName('button')
    for (let i = 0; i < butts.length; i++) {
      butts[i].addEventListener('click', () => {
        this.show()
      })
    }
  },
  methods: {
    show() {
      event.preventDefault()
      this.$modal.show(
        Lightbox,
        {
          image: event.target.dataset.src
        },
        {
          name: 'lightbox',
          height: 'auto',
          width: '75%',
          transition: 'expand',
          classes: ['v--modal', 'v--modal-lightbox']
        }
      )
    },
    hide() {
      this.$modal.hide('lightbox')
    }
  }
}
</script>

